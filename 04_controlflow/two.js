// Switch case

const month = 3

switch (month) {
    case 1:
        console.log('January')
        break;  // stops the execution onece the case is matched except default
    case 2:
        console.log('Feb')
        break;
    case 3:
        console.log('March')
        break;
    case 4:
        console.log('April')
        break;

    default: 
        console.log('default case match')
        break;
}